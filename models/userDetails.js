const mongoose = require('mongoose');

const userScheema = mongoose.Schema({
    name: {
        type: String,
        required: true,
        trim: true
    },
    email: {
        type: String,
        required: true,
        trim: true
    },
    phone: {
        type: Number,
        required: false,
    },
    password: {
        type: String,
        required: true,
        trim: true
    },
    role: {
        type: String,
        required: true,
        trim: true
    }
})

const UserDetails = mongoose.model('userdetails', userScheema);
module.exports = UserDetails;
